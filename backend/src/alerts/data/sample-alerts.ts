// Helper to create a date from days ago in 2025
const daysAgo = (days: number): Date => {
  const date = new Date('2025-11-25T00:00:00Z'); // Base date: November 25, 2025
  date.setDate(date.getDate() - days);
  return date;
};

// Helper to create a date in a specific month/year
const createDate = (year: number, month: number, day: number, hour: number = 12): Date => {
  return new Date(`${year}-${String(month).padStart(2, '0')}-${String(day).padStart(2, '0')}T${String(hour).padStart(2, '0')}:00:00Z`);
};

// Helper to calculate days to ETA from a base date
const calculateDaysToEta = (orderDate: Date, plannedEta: Date): number => {
  const now = new Date('2025-11-25T00:00:00Z'); // Base date
  const diffMs = plannedEta.getTime() - now.getTime();
  return Math.ceil(diffMs / (1000 * 60 * 60 * 24));
};

// Helper to calculate days between two dates
const daysBetween = (date1: Date, date2: Date): number => {
  const diffMs = date2.getTime() - date1.getTime();
  return Math.ceil(diffMs / (1000 * 60 * 60 * 24));
};

